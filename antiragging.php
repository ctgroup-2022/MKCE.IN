                    <h3>Anti-Ragging Committee:</h3>
                    <p align="justify">We, MKCE, Shahpur, have zero tolerance for ragging and proud to be a campus that is ragging free. Students are educated to follow the act and ensure a ragging free campus. Anti-ragging Committee has been constituted to prohibit, prevent and eliminate the scourge of ragging. To ascertain compliance as per the UGC Regulations on curbing menace of ragging in higher educational institutions, 2009, the Anti-ragging Committee is nominated and headed by the Principal, and comprises of representatives as prescribed by the UGC Regulations, including civil and police administration, local media, Non-Government Organizations involved in youth activities and representatives from the institute.</p>
                    <p align="justify">A. The committee will ensure compliance with the provisions of the Anti-ragging regulations; monitor and oversee the performance of the Anti-Ragging Squad in prevention of ragging in the campus. Anti-Ragging Policy Government/University Grants Commission (UGC), guidelines notified vide no.F.1-16/2009 (CPP-II) dated 21-10-2009 on Curbing the Menace of Ragging in Higher Educational Institutions, 2009 (Under Section 26(1) (g) of the University Grants Commission Act, 1956) are strictly implemented at MKCE, Shahpur. According to the Hon’ble Supreme Court of India, Ragging constitutes one or more of any of the following acts which may cause or is likely to cause physical, emotional, social, intellectual or psychological harm or to raise fear or apprehension thereof in a fresher or a junior student. It includes:</p>
                    <ol>
                        <li>Any conduct by any student individual or collective whether verbal (oral or written) or non-verbal or by an act which has the effect of teasing, threatening or terrorizing or an act of treating or handling with rudeness a fresher or any other student.</li>
                        <li>Indulging in rowdy or indiscipline activities by any student or students which causes or is likely to cause annoyance, hardship, physical, emotional or psychological harm or to raise fear or apprehension thereof in any fresher or any other student.</li>
                        <li>Asking any student to do any act which such student will not engage in the ordinary course and which has the effect of causing or generating a sense of shame, torment or embarrassment so as to adversely affect the physique or psyche of such fresher or any other student.</li>
                        <li>Any act by a senior student that prevents, disrupts or disturbs the regular academic activity of any other student or a fresher.</li>
                        <li>Exploiting the services of a fresher or any other student for completing the academic tasks assigned to an individual or a group of students.</li>
                        <li>Any act of financial extortion or forceful expenditure burden put on a fresher or any other student by students.</li>
                        <li>Any act of physical abuse including all variants of it: sexual abuse, homosexual assaults, and stripping, forcing obscene and lewd acts, gestures, causing bodily harm or any other danger to health or person.</li>
                        <li>Any act or abuse by spoken words, emails, post, public insults which would also include deriving perverted pleasure, vicarious or sadistic thrill from actively or passively participating in the discomfiture to fresher or any other student.</li>
                        <li>Any act that affects the mental health and self-confidence of a fresher or any other student with or without an intent to derive a sadistic pleasure or showing off power, authority or superiority by a student over any fresher or any other student.</li>
                    </ol>
                    <p align="justify">B. In cast of emergency the students / parents in distress owing to ragging may use National Anti-Ragging Helpline 1800-180-5522 (Toll Free) Website : www.antiragging.in, Email : helpline@antiragging.in</p>
                    <p align="justify">Ragging is totally banned in the College and anyone found guilty of ragging and/or abetting ragging is liable to be punished as it is a criminal offence.</p>
